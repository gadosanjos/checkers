    <section>
        <h1>Introductory Rules for Checkers</h1>
        <p>The game of Checkers (also called Draughts) is approximately four thousand years old. It was played by the ancient Egyptians almost as it is played today. The game is known in every country around the world and, oddly enough, was even played by the inhabitants of New Zealand before the first white people arrived in that land.</p>
        
        <h2>The Board</h2>
        <p>The checkerboard is divided into 64 alternating light and dark squares.</p>
        <p>Checkers is played on squares of one color only, and it is common practice to play on the darker-colored squares.</p>

        <h2>The Men</h2>
        <p>There are twenty-four pieces, with one player using twelve light-colored pieces and the other player using twelve dark-colored pieces.</p>
        
        <h2>Setup</h2>
        <p>The board is set between the two players so that each player has a dark corner space at his left, when the play is on the dark squares.</p>
        <p>Each player places his twelve pieces on the dark squares in the first three rows in front of him. This leaves clear the two center rows.</p>

        <h2>Objective</h2>
        <p>The objective of the game is to capture all of the opponent's pieces or confine them so that the opponent has no possible moves left.</p>
        <p>If neither player can achieve these objectives, the game becomes a draw.</p>

        <h2>Play</h2>
        <p>For the first game, draw lots to determine which player has the choice of the color of pieces. In subsequent games, players alternately use the dark and light sets of pieces.</p>
        <p>The dark pieces always have the first move.</p>
        <p>The movement of one piece by a player, employing either the Simple Move or Jump, constitutes their turn.</p>

        <h2>Simple Move</h2>
        <p>As the pieces never leave the color upon which they are first placed, all moves must be diagonal. A piece can move only one square at a time and only to a square diagonally in front of it (in the direction of its opponent's side of the board).</p>
        
        <h2>Jump</h2>
        <p>If the square to which a piece might move is occupied by an opponent's piece, this piece may jump to the opponent's piece if there is a vacant square immediately beyond it. Two or more pieces may sometimes be jumped in succession on a single turn if the opponent's pieces are in the proper position.</p>
        <p>All pieces jumped are removed from the board.</p>
        <p>When a player has an opportunity to jump an opponent's piece, they MUST do so.</p>
        <p>If a player has two or more different opportunities to jump on a turn, they may choose the jump they prefer.</p>
        <p>If a player overlooks a jump or refuses it, the opponent can do one of three things:</p>
        <ol>
            <li>Compel that player to take back their move and make the jump.</li><br>
            <li>Remove from the board the piece that should have made the jump. (This is called the "Huff" and does not count as a regular move.)</li><br>
            <li>Let the move stand as is.</li><br>
        </ol>

        <h2>King</h2>
        <p>When a piece reaches one of the squares in the last row across the board from where it started, it is crowned a King by placing one of the previously captured pieces of the same color on it.</p>
        <p>A King can move and jump diagonally either forward or backward.</p>

